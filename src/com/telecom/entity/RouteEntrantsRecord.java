package com.telecom.entity;

import javax.persistence.Entity;

/**
 * 实体类 - 足迹参与record
 */

@Entity
public class RouteEntrantsRecord extends BaseEntity {

	/**
	 * 
	 */
	private static final long serialVersionUID = -4814845559093102243L;

	private String routeCreatorRecord;// 路线id
	private String routeEntrants;// 路线参与者
	private String scenicSpot;// 参与景点
	private String imgUrl;// 上传景点照片

	public String getRouteCreatorRecord() {
		return routeCreatorRecord;
	}

	public void setRouteCreatorRecord(String routeCreatorRecord) {
		this.routeCreatorRecord = routeCreatorRecord;
	}

	public String getRouteEntrants() {
		return routeEntrants;
	}

	public void setRouteEntrants(String routeEntrants) {
		this.routeEntrants = routeEntrants;
	}

	public String getImgUrl() {
		return imgUrl;
	}

	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}

	public String getScenicSpot() {
		return scenicSpot;
	}

	public void setScenicSpot(String scenicSpot) {
		this.scenicSpot = scenicSpot;
	}
}
